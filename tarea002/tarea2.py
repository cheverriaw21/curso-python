#Hallar la Potencia
print("==POTENCIA==")

#Entrada de datos
a = int(input("Introduzca un numero entero positivo para la Base:"))
n = int(input("Introduzca un numero entero positivo para el Exponente:"))

#Proceso para encontrar la potencia
p = a**n

#Mostrar el resultado
print(f"La potencia es: {p}")